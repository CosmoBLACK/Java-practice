
public class Variable01 {
	public static void main(String[] args) {
		int num1;//int型の変数num1の宣言
		String str1;//String型の変数str1の宣言
		num1 = 10;//num1に初期値を代入
		str1 = "Hellow World!";//str1に初期値を代入
		System.out.println(num1);
		System.out.println(str1);
		
		num1 = 20;//num1に再代入
		System.out.println(num1);
		
		int num2 = 30;//int型の変数num2の宣言及びnum2に初期値を代入
		System.out.println(num2);
	}
}
